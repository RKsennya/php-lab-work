/**
 * Функция для выполнения арифметических операций.
 *
 * @param int $a Первое число для операции.
 * @param int $b Второе число для операции.
 * @return array Результаты операций в виде массива.
 */
function arithmeticOperations($a, $b) {
    return [
        'Сложение' => $a + $b,
        'Вычитание' => $a - $b,
        'Умножение' => $a * $b,
        'Деление' => $a / $b,
        'Остаток от деления' => $a % $b
    ];
}

